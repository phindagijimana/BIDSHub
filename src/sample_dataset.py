"""
"Try with sample data" loader.

Lets a researcher who just cloned the repo see real data flowing through
the app (Browse Subjects -> QC -> Viewer) without configuring any remote
credentials.

The bundled dataset lives at ``assets/sample_bids/`` and is generated by
``scripts/build_sample_bids.py``. It's tiny (~50 KB of synthetic NIfTI)
but structurally valid BIDS.

The loader is idempotent: clicking the button a second time finds the
existing demo dataset and returns it instead of re-inserting.
"""

from __future__ import annotations

from pathlib import Path
from typing import Optional, Tuple

# Resolved at import time so app.py doesn't need to know the path.
SAMPLE_BIDS_ROOT = (Path(__file__).resolve().parent.parent / "assets" / "sample_bids").resolve()
SAMPLE_DATASET_NAME = "BIDSHub Sample"


def sample_dataset_available() -> bool:
    """True if the bundled sample dataset is present on disk.

    Returns False if someone deleted ``assets/sample_bids/`` after install —
    we use this to hide the demo button rather than fail with a 404.
    """
    return (SAMPLE_BIDS_ROOT / "dataset_description.json").is_file()


def load_sample_dataset(db, bids_loader_cls) -> Tuple[int, int, Optional[str]]:
    """Register the bundled sample BIDS dataset in ``db``.

    Idempotent: if a dataset already exists at this name+path, returns its
    id without re-inserting subjects or scans.

    Args:
        db: an initialized ``src.database.Database`` instance.
        bids_loader_cls: ``src.bids_loader.BIDSLoader`` (injected so this
            module doesn't pull in pybids at import time — keeps the smoke
            test for ``src.sample_dataset`` cheap).

    Returns:
        (dataset_id, subject_count, error). ``error`` is None on success.
    """
    if not sample_dataset_available():
        return 0, 0, (
            f"Sample dataset not found at {SAMPLE_BIDS_ROOT}. Run "
            "`python scripts/build_sample_bids.py` to generate it."
        )

    # Idempotency: don't double-register. add_dataset has a UNIQUE(name)
    # constraint so the second insert would fail silently — find any
    # existing row first.
    for ds in db.get_all_datasets() or []:
        if ds.get("name") == SAMPLE_DATASET_NAME:
            return ds["id"], ds.get("subject_count", 0) or 0, None

    try:
        loader = bids_loader_cls(str(SAMPLE_BIDS_ROOT))
    except Exception as e:  # pybids / BIDS structure error
        return 0, 0, f"Failed to load sample BIDS: {e}"

    dataset_id = db.add_dataset(
        name=SAMPLE_DATASET_NAME,
        platform="local",
        root_path=str(SAMPLE_BIDS_ROOT),
    )
    if not dataset_id:
        return 0, 0, "Could not register sample dataset in the database."

    subjects = loader.get_subjects()
    for subject in subjects:
        # pybids returns bare local IDs ('01'); store the BIDS-prefixed form
        # for the canonical subject_id column.
        bids_subject = f"sub-{subject}" if not subject.startswith("sub-") else subject

        db.add_subject(
            subject_id=bids_subject,
            dataset_id=dataset_id,
            local_subject_id=subject,
        )

        for session in loader.get_sessions(subject=subject):
            scans = loader.get_subject_scans(subject, session)
            # pybids returns bare session IDs ('01'); BIDS path convention is 'ses-01'.
            session_label = f"ses-{session}" if session and not session.startswith("ses-") else (session or "ses-01")

            for scan in scans:
                db.add_scan(
                    subject_id=bids_subject,
                    dataset_id=dataset_id,
                    session=session_label,
                    modality=scan.get("modality") or "",
                    file_path=scan.get("file_path") or "",
                    suffix=scan.get("suffix") or "",
                    file_size_bytes=_safe_filesize(scan.get("file_path")),
                    is_downloaded=True,  # bundled with the repo, no download needed
                )

            if scans:
                db.add_subject_session(
                    subject_id=bids_subject,
                    dataset_id=dataset_id,
                    session_id=session_label,
                    scan_count=len(scans),
                )

    return dataset_id, len(subjects), None


def _safe_filesize(path: Optional[str]) -> int:
    """Return file size in bytes, or 0 if the path is missing or unreadable."""
    if not path:
        return 0
    try:
        return Path(path).stat().st_size
    except OSError:
        return 0
